#include "libft.h"

int		ft_is_int_size(char *arg)
{
	long int	tmp;
	int			i;

	i = 0;
	if (*arg == '-' || *arg == '+')
		i++;
	while (arg[i] == '0')
		i++;
	tmp = ft_atoil(arg);
	if (ft_strlen(arg + i) > 20 || tmp < -2147483648 || tmp > 2147483647)
		return (0);
	return (1);
}
